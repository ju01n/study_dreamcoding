// typeof : 데이터 타입 확인
// 값을 타입 문자열로 반환
let variable;
console.log(typeof variable);

variable = '';
console.log(typeof variable);

variable = 0;
console.log(typeof variable);

variable = {};
console.log(typeof variable);

variable = function(){};
console.log(typeof variable);

variable = Symbol();
console.log(typeof variable);